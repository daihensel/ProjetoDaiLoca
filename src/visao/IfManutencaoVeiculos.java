    public void defineCodigoVeiculo(int cod) {

        codVeiculo = cod;
        JTable aux = new JTable();
        List<Veiculosstatus> l = Popula.popularTabelaVeiculo(cod, String.valueOf(cod), aux, "");
        for (Veiculosstatus lin : l) {
            Veiculosstatus v = lin;
            tfDescricaoVeiculo.setText(v.getDescricaoVeiculo());
            tfTipoVeiculo.setText(v.getDescricaoTipo());
            tfMarca.setText(v.getMarca());
            tfAnoModelo.setText(String.valueOf(v.getAnoModelo()));
            tfAnoFabricacao.setText(String.valueOf(v.getAnoFabricacao()));
            tfValorDiaria.setText(String.valueOf(v.getValorDiaria()));
            tfKmAtual.setText(String.valueOf(v.getKmAtual()));
        }
    }

    public void defineCodigoManutencao(int cod) {

        codManutencao = cod;
        JTable aux = new JTable();
        List<Manutencao> l = Popula.popularTabelaManutencao(cod, String.valueOf(cod), aux);
        for (Manutencao lin : l) {
            Manutencao m = lin;
            dcDataManut.setDate(m.getDtManutencao());
            dcDataRetorno.setDate(m.getDtRetorno());
            taDefeito.setText(m.getMotivo());
            taSolucao.setText(m.getSolucao());
            tfObservacao.setText(m.getObservacao());
            this.defineCodigoFornecedor(m.getPessoajuridica().getPessoaIdpessoa());
            this.defineCodigoVeiculo(m.getVeiculo().getIdveiculo());
        }
        habilitaCampos(true);
        btEditar.setEnabled(false);
        btSalvar.setEnabled(true);
        btNovo.setEnabled(false);
    }

    public void defineCodigoFornecedor(int cod) {

        codFornecedor = cod;

        JTable aux = new JTable();
        List<Populartabelafornecedor> dadosForn = Popula.popularTabelaFornecedor(cod, String.valueOf(cod), aux);

        for (Populartabelafornecedor lin : dadosForn) {
            tfRazaoSocial.setText(lin.getNome());
            tfCNPJ.setText(lin.getCnpj());
            tfTelefone.setText(lin.getDescricaocontato());
            tfEndereco.setText(lin.getDescricaoendereco());
            tfBairro.setText(lin.getBairro());
            tfCidade.setText(lin.getDescricaocidade());
        }

    }

    public void limpaCampos() {
        limpaCampos lc = new limpaCampos();
        lc.limparCampos(jpFornecedor);
        lc.limparCampos(jpManut);
        lc.limparCampos(jpVeiculo);
        dcDataManut.setDate(null);
        dcDataRetorno.setDate(null);
    }

    private void habilitaCampos(boolean tf) {
        if (tf == false) {
            limpaCampos();
            btSalvar.setEnabled(false);
            btEditar.setEnabled(true);
            btNovo.setEnabled(true);
        }
        dcDataManut.setEnabled(tf);
        dcDataRetorno.setEnabled(tf);
        tfObservacao.setEnabled(tf);
        taSolucao.setEnabled(tf);
        taDefeito.setEnabled(tf);
        btPVeiculo.setEnabled(tf);
        btPFornecedor.setEnabled(tf);
    }

