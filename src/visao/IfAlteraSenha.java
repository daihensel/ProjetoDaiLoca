    private void btAlteraSenhaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlteraSenhaActionPerformed
        if (pfSenhaAtual.getPassword().length > 0 && pfNovaSenha.getPassword().length > 0 && pfConfNovaSenha.getPassword().length > 0) {
            String senhaatual = new String(pfSenhaAtual.getPassword());
            String senhanova = new String(pfNovaSenha.getPassword());
            String senhanovaconfirma = new String(pfConfNovaSenha.getPassword());
            
            if (Utility.alteraSenha(loginuser, senhaatual, senhanova, senhanovaconfirma) == true) {
                JOptionPane.showMessageDialog(null, "Senha alterada com sucesso!");
                pfSenhaAtual.setText("");
                pfNovaSenha.setText("");
                pfConfNovaSenha.setText("");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos...", "Atenção", JOptionPane.ERROR_MESSAGE);
            
        }
    }//GEN-LAST:event_btAlteraSenhaActionPerformed

