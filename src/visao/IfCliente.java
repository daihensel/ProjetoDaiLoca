    public void limpaCampos() {
        tfDataCadastro.setText(Formatacao.getDataAtual());
        tfNome.setText("");
        tfTelefone1.setText("");
        tfCPF.setText("");
        tfRG.setText("");
        tfCNPJ.setText("");
        tfIE.setText("");
        tfEndereco.setText("");
        tfBairro.setText("");
        tfCEP.setText("");
        tfCidade.setText("");
        tfComplemento.setText("");
        btPCidade.setText("");
        tfUF.setText("");
        rbPessoaJuridica.setSelected(false);
        rbPessoaFisica.setSelected(false);
        Formatacao.reformatarCnpj(tfCNPJ);
        Formatacao.reformatarIe(tfIE);
        Formatacao.reformatarCEP(tfCEP);
        Formatacao.reformatarTelefone(tfTelefone1);
        Formatacao.reformatarRG(tfRG);
        Formatacao.reformatarCpf(tfCPF);
    }

    public void pesquisa() {
        int cod = 0;
        if (tfPesquisa.getText().length() > 0 && tfPesquisa.getText().matches("[0-9]")) {
            cod = Integer.parseInt(tfPesquisa.getText());
        }
        Popula.popularTabelaCliente(cod, tfPesquisa.getText(), tbClientes);
    }

