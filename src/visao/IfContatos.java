    public void defineCodigoFuncionario(int cod) {
        codFuncionario = cod;
        Popula.popularTabelaContatos(codFuncionario, "", tbFuncionario);

        JTable aux = new JTable();
        List<Populartabelafuncionario> l = Popula.popularTabelaFuncionario(cod, String.valueOf(cod), aux);
        for (Populartabelafuncionario lin : l) {
            tfNomeFunc.setText(lin.getNome());
            tfRGFunc.setText(lin.getRg());
            tfCPFFunc.setText(lin.getCpf());
            tfTelefoneFunc.setText(lin.getDescricaocontato());
            tfFuncaoFunc.setText(lin.getDescricaofuncao());
            tfLoginFunc.setText(lin.getLogin());
        }
    }

