    public void habilitaCampos(Boolean tf) {
        if (tf == false) {
            limpaCampos();
        }
        tfDescricao.setEnabled(tf);
        cbTipoVeiculo.setEnabled(tf);
        tfDtBaixa.setEnabled(tf);
        tfAnoFab.setEnabled(tf);
        tfAnoModelo.setEnabled(tf);
        tfKmAtual.setEnabled(tf);
        tfMarca.setEnabled(tf);
        cbStatusVeiculo.setEnabled(tf);

    }

    public void limpaCampos() {
        tfDescricao.setText("");
        populaCombos();
        cbTipoVeiculo.setSelectedIndex(0);
        tfDtInclusao.setText(Formatacao.getDataAtual());
        tfDtBaixa.setText("");
        tfAnoFab.setText("");
        tfAnoModelo.setText("");
        tfKmAtual.setText("");
        tfMarca.setText("");
        cbStatusVeiculo.setSelectedIndex(0);

    }

    public void pesquisa() {
        int cod = 0;
        if (tfPesquisa.getText().length() > 0 && tfPesquisa.getText().matches("[0-9]")) {
            cod = Integer.parseInt(tfPesquisa.getText());
        }
        Popula.popularTabelaVeiculo(cod, tfPesquisa.getText(), tbVeiculos);
    }

    public void populaCombos() {
        cbTipoVeiculo.removeAllItems();
        new CombosDAO().popularCombo("Tipoveiculo", "idtipoVeiculo", "descricao", cbTipoVeiculo, "");
        cbStatusVeiculo.removeAllItems();
        new CombosDAO().popularCombo("Statusveiculo", "idstatusveiculo", "descricao", cbStatusVeiculo, "");

    }


